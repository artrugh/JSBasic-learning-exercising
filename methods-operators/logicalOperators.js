// the AND operator (&&) returns TRUE if both expressions are true, otherwise FALSE

val = x < 10 && y < 200;

console.log(val);

// the OR operator (||) returns TRUE if one or both expressions are true, FALSE otherwise

val = x == 10 || y == 5;

console.log(val);

val = x == 30 || y == 7;

console.log(val);

// the NOT operator (!) returns FALSE for TRUE statements ans TRUE for FALSE statements

val = x != 30;
val = !(x > 50);

console.log(val);




